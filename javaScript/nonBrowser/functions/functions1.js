function add(a, b) {
  console.log("A + B = ", a + b);
}

function multiply(a, b) {
  console.log("A * B = ", a * b);
}

function subtract(a, b) {
  console.log("A - B = ", a - b);
}

function divide(a, b) {
  console.log("A / B = ", a / b);
}

function remainder(a, b) {
  console.log("A % B = ", a % b);
}

a = 33;
b = 44;

add(a, b);
multiply(a, b);
subtract(a, b);
